
//Java program to illustrate While Loop

public class _1_While_Demo 
{

	public static void main(String[] args) 
	{
	   int x=1;
	   //Exit when x becomes greater than 4
	   while(x<=4)
	   {
		   System.out.println("value of x : "+ x);
		   //increment the value of x for
		   //next iteration
		   x++;
		}

	}

}
